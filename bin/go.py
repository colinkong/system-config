#!/usr/bin/env python3
"""
GO compiler wrapper (sets GOROOT automatically)
"""

import glob
import os
import signal
import sys

import command_mod
import subtask_mod


class Main:
    """
    Main class
    """

    def __init__(self) -> None:
        try:
            self.config()
            sys.exit(self.run())
        except (EOFError, KeyboardInterrupt):
            sys.exit(114)
        except SystemExit as exception:
            sys.exit(exception)

    @staticmethod
    def config() -> None:
        """
        Configure program
        """
        if hasattr(signal, 'SIGPIPE'):
            signal.signal(signal.SIGPIPE, signal.SIG_DFL)
        if os.name == 'nt':
            argv = []
            for arg in sys.argv:
                files = glob.glob(arg)  # Fixes Windows globbing bug
                if files:
                    argv.extend(files)
                else:
                    argv.append(arg)
            sys.argv = argv

    @staticmethod
    def run() -> int:
        """
        Start program
        """
        golang = command_mod.Command(os.path.join('bin', 'go'), errors='stop')
        golang.extend_args(sys.argv[1:])

        goroot = os.path.dirname(os.path.dirname(golang.get_file()))
        if os.path.isdir(os.path.join(goroot, 'pkg')):
            os.environ['GOROOT'] = goroot

        subtask_mod.Exec(golang.get_cmdline()).run()

        return 0


if __name__ == '__main__':
    if '--pydoc' in sys.argv:
        help(__name__)
    else:
        Main()
